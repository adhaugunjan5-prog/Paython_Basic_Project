import numpy as np

# a) Numbers from 0 to 20 (step 1)
arr1 = np.arange(0, 21, 1)

# b) Even numbers from 10 to 50
arr2 = np.arange(10, 51, 2)

# c) Numbers from 5 to 100 with step of 5
arr3 = np.arange(5, 101, 5)

# Printing arrays with labels
print("Numbers from 0 to 20:")
print(arr1)

print("\nEven numbers from 10 to 50:")
print(arr2)

print("\nNumbers from 5 to 100 with step 5:")
print(arr3)