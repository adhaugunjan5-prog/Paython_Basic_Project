import numpy as np

# a) 1D array of 8 zeros
zeros_1d = np.zeros(8)

# b) 2D array of shape (4,4) filled with ones
ones_2d = np.ones((4, 4))

# c) 3x3 matrix of zeros
zeros_matrix = np.zeros((3, 3))

# Printing arrays with labels
print("1D Array of 8 zeros:")
print(zeros_1d)

print("\n2D Array (4x4) of ones:")
print(ones_2d)

print("\n3x3 Matrix of zeros:")
print(zeros_matrix)